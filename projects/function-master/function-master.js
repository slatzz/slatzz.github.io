function capitalizeWord(string) {
    
    return string[0].toUpperCase();
}

//////////////////////////////////////////////////////////////////////
// Function 6 - Capitalize All Words /////////////////////////////////
//////////////////////////////////////////////////////////////////////

function capitalizeAllWords(string) {
    
    for(let i = 0; i < string.length; i++){
        
        return string[i][0].toUpperCase();
    }
    
    return string;
    
}

