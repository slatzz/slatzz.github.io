// Function 3 - Values to String /////////////////////////////////////
//////////////////////////////////////////////////////////////////////

function valuesToString(object) {
    
    // Convert object values to strings in array
        // Use join method to extract strings from array
    return Object.values(object).join(" ");
    
}

